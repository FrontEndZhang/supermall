<template>
  <div class="tab-control">
    <div @click="itemClick(index)" v-for="(item , index) in titles" :key="index" class="tab-control-item" :class="{active : index === currentIndex}">
      <span>{{item}}</span>
    </div>
  </div>
</template>

<script>
export default {
    name : 'TabControl',
    props : {
      titles : {
        type : Array,
        default(){
          return []
        }
      }
    },
    data() {
      return {
        currentIndex : 0
      }
    },
    methods: {
      itemClick(index){
        this.currentIndex = index
        this.$emit('tabClick', index)
      }
    },

};
</script>
<style scoped>
  .tab-control {
    display: flex;
    text-align: center;

    position: sticky;
    top: 44px;
  }
  .tab-control-item  {
